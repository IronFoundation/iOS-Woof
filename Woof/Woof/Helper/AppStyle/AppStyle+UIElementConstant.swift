import Foundation

extension AppStyle {
    /// A utility enum defining various sizes for UI elements.
    enum UIElementConstant {
        /// The default stroke size used for borders.
        static let borderStroke = 12.0

        /// The default corner radius size used for rounded corners.
        static let cornerRadius = 10.0

        /// The minimum padding size.
        static let minPadding = 4.0

        /// The default side size of an avatar image.
        static let avatarSideSize = 60.0

        /// The default spacing that can be used between elements in View.
        static let spacingBetweenElements = 8.0

        /// Opacity level for progress view background
        static let opacityLevelForProgressViewBackground = 0.4

        /// The wide spacing size.
        static let wideSpacingSize = 20.0
    }
}
